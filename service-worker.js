const ALL_RESOURCES = [...ASSETS, ...AUDIO_URLS, ...TILES];

self.addEventListener("install", event => {
  self.skipWaiting();
  event.waitUntil(
    caches.open(CACHE_NAME).then(cache => {
      // We use cache.addAll but handle failures gracefully to prevent SW install crash
      return Promise.allSettled(
        ALL_RESOURCES.map(url => {
          return cache.add(url).catch(err => {
            console.warn(`Failed to cache on install: ${url} - ${err.message}`);
          });
        })
      );
    })
  );
});

self.addEventListener("activate", event => {
  event.waitUntil(
    caches.keys().then(keys => {
      return Promise.all(
        keys.map(key => {
          if (key !== CACHE_NAME) {
            return caches.delete(key);
          }
        })
      );
    }).then(() => self.clients.claim())
  );
});

// Cache-first strategy with fallback
self.addEventListener("fetch", event => {
  event.respondWith(
    caches.match(event.request).then(cachedResponse => {
      if (cachedResponse) {
        return cachedResponse;
      }
      return fetch(event.request).then(networkResponse => {
        if (!networkResponse || networkResponse.status !== 200) {
          return networkResponse;
        }
        // Cache dynamically requests that are in the same origin (like tiles and icons)
        const url = event.request.url;
        if (url.startsWith(self.location.origin) || url.includes("supabase.co") || url.includes("unpkg.com")) {
          const responseToCache = networkResponse.clone();
          caches.open(CACHE_NAME).then(cache => {
            cache.put(event.request, responseToCache);
          });
        }
        return networkResponse;
      }).catch(err => {
        console.error("Fetch error:", err);
      });
    })
  );
});
